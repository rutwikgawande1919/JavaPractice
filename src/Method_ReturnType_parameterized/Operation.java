package Method_ReturnType_parameterized;

public class Operation {
	
	public int Addition(int a,int b) {
		int addition=a+b;
		return addition;
	}
	
	public int Substracrion(int a, int b) {
		int Substract=a-b;
		return Substract;
	}
	
	public int Division(int a,int b) {
		int division=a/b;
		return division;
	}
	
	public int Mod(int a,int b) {
		int mod=a%b;
		return mod;
	}

}
