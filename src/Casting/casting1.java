package Casting;

public class casting1 {
public static void main(String[] args) {
	int a =12;
	double b=a;
	System.out.println(b);
	
	float c=55.77f;
	double d=c;
	System.out.println(d);
	
	long n=9857743328l;
	short f=(short) n;
	System.out.println(f);
	
	double j=88.90;
	int r=(int) j;
	System.out.println(r);
}
}
