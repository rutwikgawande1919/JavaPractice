package Conditions;

public class Nested_IF_ELSE_Condition {

	public static void main(String[] args) {

		int a =12;
		
		if(a % 2 ==0) {
			System.out.println("It is a even number");
			
		if(a % 3 ==0) {
			System.out.println("It is a good number");
		}	
		}
		else {
			System.out.println("It is a bad number");
		}
		
	}

}
