package Conditions;

public class Ladder_elseif {
public static void main(String[] args) {
	int a =34;
	
	if(a>=80) {
		System.out.println("Grade A");
	}else if(a>=60) {
		System.out.println("Grade B");
	}else if(a>=35) {
		System.out.println("Grade C");
	}else {
		System.out.println("Fail");
	}
}
}
